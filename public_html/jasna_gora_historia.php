<b>III. Rys Historyczny twierdzy Jasnogórskiej (cz. II)</b>
<p class="p1">

Między 1735 a 1750 r. forteca Jasnogórska była biernym świadkiem
smutnych wypadków, które rozgrywały się pod jej murami. Wojska
rosyjskie, ścigając rozproszone oddziały konfederatów dzikowskich,
zajęły i spustoszyły dobra klasztorne. Pobici zaś i upadli na duchu
przywódcy konfederacyi: Tarło, Ożarowski, Świdziński szukali
schronienia w murach twierdzy i znaleźli u zakonników gościnne
przyjęcie i spokojny przytułek. W kilka lat potem przesunęły się znów
pod Jasną Górą gospodarujące w Polsce jak we własnym kraju wojska
austryackie pod wodzą feldmarszałka Laudona. Niedługo trwał spokój w
pierwszych latach panowania Stanisława Augusta. Już w 1770 r.
zawierucha konfederacyi barskiej, ogarniając stopniowo kraj cały,
przeniosła się również i w okolice Częstochowy. Puławski na czele swego
niewielkiego, lecz dzielnego oddziału, upatrzył sobie i wybrał twierdzę
Jasnogórską jako stały i najpewniejszy punkt oparcia, jako podstawę
operacyjną dla porozrzucanych dokoła oddziałów partyzanckich.
Urzeczywistnienie tego zamiaru nie poszło mu jednak tak łatwo, ponieważ
Paulini, nauczeni niejednokrotnem doświadczeniem, starali się ile
możności zachować jak najściślejszą neutralność i nie chcieli brać
bezpośredniego udziału w ówczesnych zamieszkach wojennych. Puławski
musiał chwycić się zwykłego w takich wypadkach środka, podstępu, aby
opanować twierdzę i umieścić w obrębie fortyfikacyi własną załogę. Po
kierunkiem światłych wskazówek dowódców konfederackich: Choisy\'ego i
Viomenila inżynierowie francuscy umacniali naprędce fortyfikacye tak,
że już w grudniu tego samego roku Puławski mógł spokojnie wytrzymać
kilkutygodniowe oblężenie Jasnej Góry przez oddział rosyjski Drewicza.
Gdy Drewicz, zniechęcony zbyt wytrwałym oporem, oddalił się z pod murów
klasztoru, Puławski, korzystając z chwilowej swobody, przedsiębrał
liczne a śmiałe wycieczki w rozmaite okolice kraju. W twierdzy
pozostawała wtedy tylko nieliczna załoga złożona z 800 konfederatów;
reszta oddziału zaś pod wodzą Puławskiego rozwijała bardzo żywą
działalność: biegła pod Poznań, ku linii Dunajca w skomplikowanych
planach Dumouri za, nieraz zapędzała się aż w okolice Lwowa. Tak trwało
wszystko do r. 1772. Z biegiem czasu jednak konfederacya zaczęła chylić
się do upadku. Twierdze, znajdujące się jeszcze w rękach konfederatów:
Tyniec i Lanckorona poddawały się jedna po drugiej; wobec tego
kapitulacya Częstochowy wydawała się również nieuniknioną.
<p class="p1">Puławski, zagrożony osobiście surowym wyrokiem trybunałów
koronnych, wymknął się za granicę. Pozostały dowódca załogi
częstochowskiej, Radzimiński, zgodził się na kapitulacyę, ale pod tym
jednym ważnym warunkiem, że warownia Jasnogórska przejdzie od razu w
ręce wojsk królewskich bez pośrednictwa wojsk cudzoziemskich. To
niezłomne postanowienie konfederatów wywołało z kolei energiczny
protest i opór ze strony dowódców rosyjskich. Stąd nowa walka. Pod
murami Jasnej Góry stoczono ostatni, rozpaczliwy bój, który wobec
przewagi sił nieprzyjacielskich musiał zakończyć się poddaniem
Częstochowy, "na dyskrecyę". Stało się to 25 sierpnia 1772 r.
