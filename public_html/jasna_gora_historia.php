<b>Janitoryum</b>

<p class="p1">Wychodząc z kaplicy Matki Boskiej głównemi drzwiami, wchodzimy
do czworobocznego krużganka, zwanego Janitoryum, ozdobionego 14- ma
stacyami Męki Pańskiej. Są tu dwa duże obrazy, jeden przedstawia epizod
z życia Estery, córki Mardocheusza, znany z historyi Starego
Testamentu; drugi- sprowadzenie zwłok św. Pawła do Węgier. Z Janitoryum
prowadzą na dziedziniec klasztorny dwoje drzwi. wścianie naprzeciw nich
znajduje się pomnik z napisem:
"Na pamiątkę pochowania kości Xięży Braci i Dobrodziejów Zakonu św.
Pawła Pustelnika. d. 13 pażdziernika 1835 r. na tem miejscu".
Szczątki ciał zebrane były z grobów kościelnych i przy uroczystem
nabożeństwie złożone tutaj, u stóp Maryi. Przy mniejszych drzwiach,
prowadzących do kaplicy Matki Boskiej z prawej strony (naprzeciwko
ołtarza Pana Jezusa) w temże Janituryum znajduje się drugi pomnik z
czarnego marmuru. Pomnik ten wzniósł dla siebie Wojciech Kazimierz z
Kurozwęk Męciński.
<p class="p1"><b>Zakrystya</b>

<p class="p1">Zakrystya i znajdujący się w niej ołtarz św. Wacława wybudowane
były z dochodów klasztornych i poświęcone przez Ludwika Stempkowskiego,
biskupa Heliopolitańskiego w 1653 r. Piękna ta sala 34 łokcie długa i
17 szeroka, ozdobioną jest słabego pędzla malowidłami,
przedstawiającymi czterech Ewangelistów i obrazami z życia
pustelniczego św. Antoniego i św. Dympfny.
Z zakrystyi jest wejście do skarbca, w którym mieści się wiele
pięknych, drogocennych i ciekawych pamiątek. Opis skarbca szczegółowy,
ozdobiony kilkudziesięciu ilustracyemi znajdzie czytelnik w osobnej
książeczce, wydanej staraniem ks. Admaczyka, redaktora "Dzwonka
Częstochowskiego".
Z Janitoryum prowadzą drzwi na korytarz klasztorny. Na tym korytarzu
porozwieszane są duże obrazy, przedstawiające wizerunki papieży, królów
węgierskich i królów polskich. <p class="p1">
