<p class="p1">Wszedłszy przez ostatnią bramę na
dziedziniec kościelny, po prawej i lewej stronie widzimy wejścia na
wały, któremi można obejść klasztor dookoła. Dalej po lewej stronie-
brama, prowadząca na podwórze klasztorne, a po prawej- brama z herbem
Potockich (Pilawa), stanowiąca wejście na były cmentarz grzebalny, na
którym znajduje się dzwonnica. Budynek wprost bramy wałowej, na którego
ścianie widzimy duży malowany obraz Matki Boskiej z klęczącym u dołu
św. Pawłem i Janem Ewangelistą, nosi nazwę <i>Pokojów królewskich</i>,
tutaj bowiem zatrzymywali się królowie podczas odwiedzin Jasnej Góry.
Pod pokojami królewskimi mieściła się apteka, założona przez
prowincyała Paulinów księdza Kłodawskiego z funduszów, hojnie
ofiarowanych przez Władysława IV, króla polskiego. Po prawej stronie
przysionka kościelnego widzimy dwie kaplice: św. Pawła i Matki Boskiej
Kodeńskiej, zakończone kopułami. W pierwszym przysionku kościoła
widzimy dwa duże portrety królów węgierskich: po lewej stronie- św.
Stefana, a po drugiej- Władysława. W przedłużeniu drugiego przysionka
znajduje się kaplica <i>św. Antoniego Padewskiego</i>,
oddzielona piękną żelazną kratą. Drzwi, prowadzące z przysionka do
głównej nawy kościoła, ozdobione są płaskorzeźbą, przedstawiającą
świętych Pawła i Antoniego. Dawniejsze drzwi wykonane były przez
braciszka zakonu Grzegorza Woźniakowskiego; gdy te jednak uległy
zniszczeniu, sprawiono nowe w 1858 r., podobne do poprzednich. Stąd
wspaniale się przedstawia widok wnętrza świątyni, 112 łokci długiej, 36
szerokiej i 50 wysokiej. Ściany mozajkowane robią wrażenie marmurowych.
<p class="p1">
Co do czasu powstania budowy, kościół można rozdzielić na dwie części:
część dawniej zbudowana jest węższa i rozciąga się od wielkiego ołtarza
do sygnaturki,- część druga, zbudowana później, podzielona jest
filarami wzdłuż na trzy nawy. W środkowej, znacznie wyższej od
bocznych, mieści się ambona i chór z wielkim organem, sprawionym przez
biskupa Szembeka, prymasa, w 1725 r., który następnie kosztem klasztoru
został odnowiony i powiększony. Malowidła na sklepieniu w presbiteryum
i na kościele wykonane przez Karola Dankwarta w 1695 r., przedstawiają
całą historyę Krzyża Świętego; kościół ten bowiem jest pod wezwaniem
Wniebowzięcia Najświętszej Maryi Panny i Znalezienia Krzyża Świętego. <p class="p1">
Szczególniejszą uwagę należy zwrócić na wielki ołtarz wzniesiony w 1726
r. z fundacyi Chomentowskiego, kosztem 50,000 złp.. Ponad pięknem
tabernakulum, wyrobionem z florenckiego marmuru, wznosi się posąg
Wniebowzięcia Najświętszej Maryi Panny. Grupa ta, zrobiona z jednej
sztuki ciosowego kamienia z Chełmskiej góry pod Wielgomłynami,
przedstawia figury naturalnej wielkości. Po bokach jej stoi czterech
apostołów, a u góry św. Trójca i wiele innych figur. <p class="p1">
<i><sup>1) Władysław książę Opolski, otrzymawszy ziemię Wieluńską,
przewiózł Obraz Matki Boskiej z zamku Bełzkiego do Polski w 1382 r. i
umieścił go w kaplicy na Jasnej Górze. Do strzeżenia świętego obrazu
tenże książę sprowadził z Węgier zakon pustelników św. Pawła. Odtąd
rozpoczyna się historya klasztoru Jasnogórskiego.</sup>
