Jakżeś spadł z nieba, Lucyferze! Obrazy te odnowił w 1872 r. Mateusz
Mączyński. <p class="p1">
Druga część kaplicy podzielona jest filarami na trzy nawy. Środkowa
znacznie wyższa, bogato zdobiona prześlicznemi gipsowemi stiukami,
między któremi znajduje się bardzo wiele obrazów fresco, treści
symbolicznej, wyjętej z Pisma Świętego, a także liczne wizerunki
świętych i obrazy przedstawiające życie Najświętszej Maryi Panny; te
ostatnie- dzieła artystów francuskiej szkoły. W górze kaplicy z trzech
jej stron porobiono galerye. W galeryi naprzeciw Cudownego Obrazu
umieszczono chór dla muzykantów i śpiewaków, oraz organy, zbudowane
przez organmistrza królewskiego Kosmowskiego w 1721 r., a odnowione
później i ozdobione figurami aniołów i św. Cecylii. Nad lewą nawą
znajduje się chór zakonny, a w nim rzeźbione stalle. Na trzeciej
galeryi mieszczą się małe organy, używane podczas Godzinek i ołtarz św.
Cecylii. Oprócz ołtarza z cudownym Obrazem, w kaplicy znajdują się
jeszcze cztery inne ołtarze.
<p class="p1">1. W lewej nawie ołtarz Matki Boskiej Bolesnej. Rzeźba z
białego marmuru przedstawia Matkę Boską, trzymającą na swem łonie
zdjęte z krzyża ciało Jezusa Chrystusa. <p class="p1">
2. W prawej nawie znajduje się ołtarz Ukrzyżowanego Jezusa Chrystusa.
Figura Chrystusa umieszczona jest na srebrnej blasze, w której wykuta
jest droga Krzyżowa. Blachę tę wykonał złotnik Szabel w 1865 r. Przed
ołtarzem zwiesza się czarna lampa, dar Lubomirskiego. Obok ołtarza wisi
Breve Papieża Piusa VI, którego mocą przy ołtarzu tym odprawiane modły
przynoszą wielkie ulgi duszom w czyścu zostającym. <p class="p1">
Inne boczne ołtarze, umieszczone przy filarach, zbudowane zostały w
1726 r. staraniem prowincyała zakonu ks. Konstantyna Moszyńskiego.
Piękne olejne obrazy, znajdujące się w tych ołtarzach, przedstawiają: z
prawej strony Narodzenie, z lewej Ofiarowanie Pana Jezusa. Obrazy te są
to kopie włoskich mistrzów, a jakkolwiek posiadają błędy w rysunku,
zaliczyć je można do najlepszych obrazów, znajdujących się na Jasnej
Górze.
W kaplicy Matki Boskiej widzimy jeszcze pięć czarnych marmurowych
nagrobków.
<p class="p1">1) W prawej nawie obok ołtarza Pana Jezusa stoi czrny
marmurowy pomnik Stanisława z Warszyc Warszyckiego. Mąż ten położył
wiele zasług dla kraju, szczególniej podczas wojen szwedzkich. Służył
za pięciu królów polskich.
<p class="p1">2) Po drugiej stronie ołtarza znajduje się pomnik Heleny z
książąt Wiśniowieckich Warszyckiej, kasztelanowej krakowskiej, zmarłej
dnia 10 marca 1660 r.
<p class="p1">3) Na tej samej ścianie nieco dalej widzimy pomnik Jana
Klemensa, hrabi z Ruszczy Branickiego. Młode lata przepędził za
granicą, szukając tam nauki i doświadczenia. Gdy Szwedzi napadli na
kraj, uważał za niegodne prawego obywatela pozostawanie dłużej za
granicą, szukając tam nauki i doświadczenia, pośpiesza więc na pomoc i
życie swoje kładzie w ofierze w 1657 r.
<p class="p1">4) W lewej nawie obok ołtarza Matki Boskiej Znajdujemy pomnik
Stanisława Józefa Krajewskiego, prezydenta trybunału duchownego. W
przeciągu 60- letniego swego żywota odznaczył się na wielu duchownych i
państwowych stanowiskach.
<p class="p1">5) Piąty pomnik, obecnie konfesyonałem zasłonięty, wzniesiony
był przez matkę dla dwudziestoletniego syna Zygmunta Grudzińskiego.
Przedwczesny zgon wiele obiecującego młodzieńca żałosnym był dla
blizkich- przez stratę tak dzielnego już żołnierza. Umarł w 1660 r.

