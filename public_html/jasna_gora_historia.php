<b>IV. </b>
<p class="p1">

<p class="p1">Każdy naród ma miejsca drogie, uświęcone cudownością pociech
duchowych. My mamy naszą Częstochowę z Cudownym obrazem Matki Boskiej.
Do niej dążyli wielcy i mali od pięciu przeszło wieków. Ona jest naszą
siłą, naszą tarczą, naszem zjednoczeniem. Rozproszeni po całym świecie
ku Niej zwracamy swe serca, i tutaj wszystkie nasze modlitwy, myśli,
westchnienia, łączą się w wspólny wianek, jak jeden głos narodu wznoszą
się przed tron Boga.
Gdyby się znalazł ktoś zaufany w harcie ducha swego i pogardzał takiemi
środkami pokrzepienia sił moralnych, niech usłyszy, co powiedział
wielki poeta niemiecki Goethe o pielgrzymkach: "Każdy krok zbliżający
pielgrzyma ku przedmiotowi jego czci, każde cierpienie męczącej podróży
i boleść nóg zranionech ostrymi kamykami i rozpalonym od słońca
piaskiem, stają się zbawiennym balsamem dla duszy jego udręczonej." Nie
dziw, że każdy, przybywając do Częstochowy, pragnie poznać Jasną Górę-
jej historyę i pamiątki, tam się znajdujące. (1)
Przybywającym na Jasną Górę zdaleka już ukazuje się klasztor w całym
swym majestacie, a przeszło 160- cio łokciowa wieża (2) od paru mil już
daje się dojrzeć. Kościół i zabudowania klasztorne obwiedzione są wałem
i obmurowane w prawidłowy czworobok z bastyonami po rogach.
Przybywający od strony miasta widzi na placu przed klasztorem figurę
św. Prokopa. Płaskorzeźba na piedestale figury przedstawia św. Prokopa,
który, według podania, zaprzągł do pługa kuszącego go czarta i
popędzając go krzyżem, orał nim skały. Wchodząc w obrąb murów
klasztornych, przechodzimy przez cztery bramy. <p class="p1">
Pierwsza z nich, najokazalsza, wzniesiona została w 1723 r. przez
Jerzego księcia Lubomirskiego, cała z kamienia ciosowego, i w
pierwotnym stanie trwa dotąd. W górnej części bramy widzimy obraz Matki
Boskiej, a nad nim kamienny posąg św. Michała, pięknie rzeźbiony, po
bokach zaś figury św. Pawła pustelnika i św. Antoniego opata. Pod
obrazem napis "Sub Tuum Praesidium" (Pod Twoją Obronę), a jeszcze niżej
herb Lubomirskich.
Przeszedłszy przez most nad pierwszym przekopem, wchodzimy w drugą
bramę, nad którą umieszczona jest biała marmurowa płaskorzeźba,
wyobrażająca popiersie króla Stanisława Poniatowskiego. W około
popiersia napis: Stanislaus Rex Pol., a obok litery S. A. R. i
wyobrażenia sztandarów. Tak ozdobioną została brama z powodu
oczekiwanego przyjazdu króla Stanisława Augusta. Poza szańcem znajduje
się brama trzecia, płaska, z kamienną figurą Matki Boskiej, której
serce przeszywa miecz. Brama ta wzniesiona została w 1851 r. za przeora
Piotra Kubarskiego zamiast dawnej zniszczonej. <p class="p1">
Drugi przekop graniczy już z murami, opasującemi wały klasztorne. Na
linii tych murów wznosi się czwarta brama najwarowniejsza, głęboko
sklepiona i opatrzona piękną żelazną kratą, niedawno sprawioną przez
kompanię warszawską. Nad bramą znajduje się malowany wizerunek
Władysława księcia Opolskiego, jako fundatora klasztoru. Przy niej
zapewne był most zwodzony.
