<p class="p1">
<p class="p1">Ściany kaplicy pokryte były dawniej wspaniałemi aksamitami
genueńskimi z XVI wieku (Velours de Genes), ofiarowanemi przez Jakuba
Sobieskiego, królewicza. Później zawieszono na ścianach ofiarowaną
przez pobożną duszę z Wołynia materyę brocart złocony, lyoński,
nowoczesnego wyrobu. Obie te materye zniszczone znajdują się obecnie w
muzeum klasztornem. Obecnie pokrywa ściany materya zwyczajna przetykana
złotem. Na ścianach porozwieszane jest mnóstwo wotów. Wyżej po prawej
stronie widzimy dwa obrazy: Ucieczkę do Egiptu i św. Józefa,
pracującego jako cieśla, odnowione w 1872 r. przez Mateusza
Mączyńskiego, ucznia b. Szkoły Sztuk Pięknych w Warszawie. Tę część
kaplicy oddziela krata żelazna, pięknie wyrobiona, około 8 łokci
wysoka. Nad bramą umieszczono siedm lichtarzy, w środku zaś kraty dwóch
aniołów trzyma Najsłodsze Imię Jezus. Krata wykonana była w Gdańsku,
skąd sprowadzoną została w 1644 .
