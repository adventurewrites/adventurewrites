Dworzańskiego i księdza Ksawerego Rotera. Na jej odbudowanie łożył
wiele ksiądz Moszyński, prowincyał zakonu. W następnych latach
odmalował ją ksiądz Anastazy Kiedrzyński. Tutaj, jako w przybytku
nauki, przedstawiono postacie świętych, którzy się odznaczyli na polu
nauki, a także postać pogańskiej bogini mądrości- Minerwy. <p class="p1">
