Prócz tego są:

<p class="p1">Historya Świata Illustrowana; Gigantomachia- opis oblężenia
Jasnej Góry przez Szwedów, skreślony przez ks. Augustyna Kordeckiego;
Antyfonarz i wiele innych cennych, na pergaminie pisanych książek.
Według opowiadania starszych Ojców Paulinów wielki nasz pisarz J. I.
Kraszewski wypożyczył z tej biblioteki dużą ilość cennych dzieł, które
mu posłużyły do prac jego naukowych. Dotąd książki te nie powróciły do
biblioteki klasztornej. Niema wcale katalogu. Tylko światły kierownik
mógłby temu podołać.
W dziedzińcu klasztornym w budynku, gdzie obecnie zamieszkuje służba,
mieściła się dawniej drukarnia klasztorna. Istniała od r. 1693
przywilej jednak na nią otrzymali paulini dopiero w 1706 r. od króla
Augusta II. W drukarni wytłaczane były książki do nabożeństwa lub opisy
dziejów Cudownego Obrazu i klasztoru. Po zamknięciu drukarni 1864 r.
prawo drukowania książek do nabożeństwa klasztor odstąpił firmie Kolm
Oderfeld w Częstochowie, która to firma do dnia dzisiejszego z tych
praw korzysta. <p class="p1">
U wejścia do wnętrza gmachu klasztornego, w przedsionku zwanym furtą
klasztorną, pozostały resztki starych malowideł na sklepieniu i
ścianach, cennej wartości, przedstawiające historyę Martynusyusza,
męczennika węgierskiego z XVI wieku. Obmurowanie klasztoru, czyli tak
zwane wały, które obecnie widzimy, jest dziełem XVIII wieku i powstało
z prywatnych fundacyi. Poprzednie mury były o kilka łokci bliżej
klasztoru. Przed 1813 rokiem wały były łokci wyższe, tak, że zakrywały
sobą okna pierwszego piętra. U stóp wałów wznoszą się <i>stacye Męki Pańskiej</i> których dotąd
jest już 10. Robota postępuje szybko. Pomysł ugrupowania figur i
wykonanie odlewów z bronzu jest dziełem artysty rzeźbiarza Piusa
Welońskiego.
