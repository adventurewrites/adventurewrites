zakonników, w niej także podejmowani bywają znakomitsi goście.
<p class="p1"><b>Sala Rycerska</b>

<p class="p1">Po wschodach wchodzimy do wspaniałej Sali Rycerskiej. Salę
zdobi dziewięć dużych obrazów, przedstawiających historyczne zdarzenia
z życia klasztoru Jasnogórskiego. Kolorowe reprodukcye tych obrazów i
szczegółowy opis ich treści znajdzie czytelnik w osobnem wydawnictwie
p.t. "Obrazy w Sali Rycerskiej" przez ks. J. Adamczyka, redaktora
"Dzwonka Częstochowskiego". Oprócz tych znajduje się tutaj jeszcze
wiele portretów wydatniejszych zakonników. Portrety i życiorysy tych
znakomitych mężów można znaleźć także w osobnym dziełku, wydanem przez
ks. J. Adamczyka, pod tytułem "Wizerunki Dostojników Duchownych". Sala
ta służyła dawniej do dysput teologicznych. Przed istniejącym tutaj
ołtarzykiem odprawiane bywały nabożeństwa dwa razy w roku: 7 marca, w
dzień św. Tomasza z Akwinu, jako patrona teologii, i 25 listopada, w
dzień św. Katarzyny, patronki.
